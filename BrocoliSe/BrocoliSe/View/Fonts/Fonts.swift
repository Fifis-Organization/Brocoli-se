import Foundation
import UIKit


extension UIFont {
    
    static func graviolaRegular(size: CGFloat) -> UIFont? {
        return UIFont(name: "Graviola-Regular", size: size)
    }
    
    static func graviolaSoft(size: CGFloat) -> UIFont? {
        return UIFont(name: "GraviolaSoft-Medium", size: size)
    }
    
}
